public class SNB_11_Increment_Decrement_Operator 
  {
      public static void main(String[] args)
       {
           int a = 10 ;
           System.out.println(a++);  // post-increment operators

           int b = 28 ;
           System.out.println(b--); // post-decrement operators  

           int c = 41 ;
           System.out.println(++c);  // pre-increment operators

           int d = 10 ;
           System.out.println(--d);  // pre-decrement operators

       }
  }
