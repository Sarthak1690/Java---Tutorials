public class SNB_09_Bitwise_Operators 
  {
      public static void main(String[] args)
        {
            int a = 10;
            int b = 20;
            System.out.println(a & b); // And operator

            System.out.println(a | b); // Or operator

            System.out.println(a ^ b); // Xor operator

        } 
  }
