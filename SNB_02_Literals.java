
public class SNB_02_Literals 
  {
     public static void main(String[] args) 
       {  

        System.out.println("Declaration of literals");

        int age = 10 ;
        System.out.println(age);

        byte  b = 10;
        System.out.println(b);  

        double d = 100D ;
        System.out.println(d);

        float  F1 = 2.90f;
        System.out.println(F1);
         
        char c = 'a';
        System.out.println(c);

        long LondDino = 10000000000000000L;
        System.out.println(LondDino);

        short s = 100;
        System.out.println(s);

        String Str = "Sarthak";
        System.out.println(Str);        
       }
  } 
