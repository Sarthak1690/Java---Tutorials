
public class SNB_05_Arithmetic_Operators 
   {
   public static void main(String[] args)
        {
            int num1 = 10;
            int num2 = 20;
                
            System.out.println(num1 + num2); // Addition operator

            System.out.println(num1 - num2); // Subtraction operator

            System.out.println(num1 * num2); // Multiplication operator

            System.out.println(num1 / num2); // Division operator

            System.out.println(num1 % num2); // Modulos operator
        }
   }