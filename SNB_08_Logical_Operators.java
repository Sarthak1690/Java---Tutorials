public class SNB_08_Logical_Operators 
  {
      public static void main(String[] args) 
         {
             int x = 10 ;
             int y = 20 ;
             int z = 30 ;
             
             System.out.println(x<y && y<z); // And operator ;

             System.out.println(x<y || y<z); // Or operator ;

             System.out.println(!(x < y)); // Not operator ;

         }  
  }
